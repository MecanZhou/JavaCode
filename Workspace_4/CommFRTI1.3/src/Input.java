import java.util.ArrayList;


public class Input{
		
		public ArrayList<String> ObjectID=new ArrayList<String>();
		public ArrayList<String> Attributes=new ArrayList<String>();	
		public ArrayList<String> getObjectID() {
			return ObjectID;
		}
		public void setObjectID(ArrayList<String> objectID) {
			ObjectID = objectID;
		}
		
		public ArrayList<String> getAttributes() {
			return Attributes;
		}
		public void setAttributes(ArrayList<String> attributes) {
			Attributes = attributes;
		}
	}